class PSHStr:
    def PSHStrEQ(self, str1, str2):
        print(str1 == str2)
    def PSHStrNE(self, str1, str2):
        print(str1 != str2)
    def PSHStrLT(self, str1, str2):
        print(str1 < str2)
    def PSHStrGT(self, str1, str2):
        print(str1 > str2)
    def PSHStrLE(self, str1, str2):
        print(str1 <= str2)
    def PSHStrGE(self, str1, str2):
        print(str1 >= str2)

